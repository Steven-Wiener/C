//*****************************************************************************
// Draws various snake chars to the LCD
// Function is sent an index of the character array to start at, and a location
// on the board to write to. All characters are 8x8pixels
//*****************************************************************************
void dogs102_write_snake(uint8_t row, uint16_t index, uint8_t col){
	uint16_t i;
	
	dogs102_set_page(row);

	// '*' starts @ 0
	// 'X' starts @ 9
	// 'o' starts @ 18
	// 'x' starts @ 27
	for(i=0; i<9; i++){
		dogs102_set_column(col*8 + i);
		dogs102_write_data(snake_Bitmaps[index + i]);
	}
}
  
//*****************************************************************************
// Function that prints out an image.
//*****************************************************************************
void draw_image(const uint8_t imageToDisplay[]){
	int i,j;
  
	dogs102_init();
	dogs102_clear();

	// Draw out various images (waitscreen, startscreen, win/lose)
	for(i = 0; i < 7; i++)	{
		dogs102_set_page(i+1);
		for(j=0; j<56; j++)	{
			dogs102_set_column(j+20);
			dogs102_write_data(imageToDisplay[i*56 + j]);
		}
	}
}
